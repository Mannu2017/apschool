package apschool.model;

public class SchoolData {
	
	private int slno;
	private String district;
	private String Location;
	private String HostelName;
	private String RegisteredEmp;
	private String RegisteredStu;
	private String ActiveEmp;
	private String ActiveStu;
	private String Date;
	private String EmpPresent;
	private String StuPresent;
	
	public int getSlno() {
		return slno;
	}
	public void setSlno(int slno) {
		this.slno = slno;
	}
	public String getDistrict() {
		return district;
	}
	public void setDistrict(String district) {
		this.district = district;
	}
	public String getLocation() {
		return Location;
	}
	public void setLocation(String location) {
		Location = location;
	}
	public String getHostelName() {
		return HostelName;
	}
	public void setHostelName(String hostelName) {
		HostelName = hostelName;
	}
	public String getRegisteredEmp() {
		return RegisteredEmp;
	}
	public void setRegisteredEmp(String registeredEmp) {
		RegisteredEmp = registeredEmp;
	}
	public String getRegisteredStu() {
		return RegisteredStu;
	}
	public void setRegisteredStu(String registeredStu) {
		RegisteredStu = registeredStu;
	}
	public String getActiveEmp() {
		return ActiveEmp;
	}
	public void setActiveEmp(String activeEmp) {
		ActiveEmp = activeEmp;
	}
	public String getActiveStu() {
		return ActiveStu;
	}
	public void setActiveStu(String activeStu) {
		ActiveStu = activeStu;
	}
	public String getDate() {
		return Date;
	}
	public void setDate(String date) {
		Date = date;
	}
	public String getEmpPresent() {
		return EmpPresent;
	}
	public void setEmpPresent(String empPresent) {
		EmpPresent = empPresent;
	}
	public String getStuPresent() {
		return StuPresent;
	}
	public void setStuPresent(String stuPresent) {
		StuPresent = stuPresent;
	}

}
